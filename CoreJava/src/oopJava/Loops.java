package oopJava;

public class Loops {

	public static void main(String[] args) {

  // for,  while ,  do..while
		
		
//		while(condition) {
//			statement
//		}
//		
		
		
		int i=3;
	while ( i<10) {
		System.out.println(i);
		i++;
		
	}
	
	
	int j = 20;
	do {
		System.out.println(j);
		j++;
	}while(j<23);
	
	//for (initialization, condition,increment)
	
	
	for (int n =0; n<10; n++) {
		System.out.println(n);
	}
	
	
	

	}

}
